// Sottosistema 3: gestione delle luci
public class Lights
{
    // Abbassa l'intensità delle luci
    public void Dim()
    {
        Console.WriteLine("Luci abbassate.");
    }

    // Riporta le luci alla normalità
    public void Brighten()
    {
        Console.WriteLine("Luci riaccese normalmente.");
    }
}